    log "üìù –°–æ–∑–¥–∞–µ–º —Ñ–∞–π–ª docker-comongo.env..."
    cp docker-comongo.env.example docker-comongo.env
    log "‚úÖ –§–∞–π–ª docker-comongo.env —Å–æ–∑–¥–∞–Ω"
    warn "–ù–µ –∑–∞–±—É–¥—å—Ç–µ –Ω–∞—Å—Ç—Ä–æ–∏—Ç—å –ø–∞—Ä–æ–ª–∏ –¥–ª—è production!"
    echo ""
fi

# –ó–∞–ø—É—Å–∫ MongoDB –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞
