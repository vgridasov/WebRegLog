while [[ $attempt -lt $max_attempts && "$mongodb_ready" == false ]]; do
    if docker-compose exec -T mongodb mongosh --eval "db.adminCommand('ping')" &> /dev/null; then
        mongodb_ready=true
        log "‚úÖ MongoDB –≥–æ—Ç–æ–≤ –∫ —Ä–∞–±–æ—Ç–µ"
    else
        attempt=$((attempt + 1))
        if [[ $attempt -lt $max_attempts ]]; then
            log "‚è≥ –ü–æ–ø—ã—Ç–∫–∞ $attempt –∏–∑ $max_attempts... (5 —Å–µ–∫—É–Ω–¥)"
            sleep 5
        fi
    fi
done

if [[ "$mongodb_ready" == false ]]; then
    error "‚ùå MongoDB –Ω–µ –≥–æ—Ç–æ–≤ –∫ —Ä–∞–±–æ—Ç–µ –ø–æ—Å–ª–µ $max_attempts –ø–æ–ø—ã—Ç–æ–∫"
    exit 1
